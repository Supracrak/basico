import math

#import=usado para importar um modulo do python
#math= modulo associado a matematica

pi= 3.14

#----------------------------------------------------------------------------------------------------
#round()
#usado para aproximar o numero ao inteiro mais proximo

print(round(pi))

#----------------------------------------------------------------------------------------------------
#math.ceil() ou #math.floor()
#ira aredondar para o proximo numero inteiro ou para o numero inteiro anterior

print(math.ceil(pi))
print(math.floor(pi))

#----------------------------------------------------------------------------------------------------
#math.sqrt()
#usado para dizer a raiz quadrada de um numero

print(math.sqrt(pi))

#----------------------------------------------------------------------------------------------------
#abs()
#usado para indicar o quao longe o numero esta em relacao ao zero

print(abs(pi))

#----------------------------------------------------------------------------------------------------
#pow(x,y)
#usado para elevar um numero x a um expoente y

print(pow(pi,2))

#----------------------------------------------------------------------------------------------------
#max() ou #min()
#usado para dizer qual o maior numero entre os dados
x=1
y=2
z=3
print(max(x,y,z))
print(min(x,y,z))
