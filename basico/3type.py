# para saber o tipo de uma varial usa-se a seguinte forma:

#STRING

name = "John"
print(type(name))

#temos entao no terminal: <class 'str'> 
#str: string 

#-------------------------------------------------------------------------------------------------

#INT

age = 12
print(type(age))

#temos no terminal:<class 'int'>
#int= integer (inteiro)

#-------------------------------------------------------------------------------------------------

#FLOAT

height= 2.55
print(height)

print(type(height))

#temos no terminal:<class 'float'>
#float= numero com casas decimais depois da virgula 

#-------------------------------------------------------------------------------------------------

#BOOL

human=True
print(type(human))

#temos no terminal:<class 'bool'>
#bool= usado para definir se algo e verdadeiro ou falso